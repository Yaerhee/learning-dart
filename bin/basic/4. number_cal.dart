void main() {
  // integer
  int number = 2;
  print(number);
  print(number * 2);

  print('---------------');
  print(number % 2); // 나머지

  print('---------------');
  number ++;
  print(number);

  print('---------------');
  number --;
  print(number);

  print('---------------');
  // double
  double number2 = 4.0;
  print(number2);

  number2 += 5;
  print(number2);

  number2 -= 1.2;
  print(number2);

  number2 /= 2;
  print(number2);
}